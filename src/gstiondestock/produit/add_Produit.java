    private void btn_add_productActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_add_productActionPerformed
        // TODO add your handling code here:
        float seuil_produit = 0;
        float prix_achat_produit = 0;
        float prix_vendre_produit = 0;
        boolean b = false;
        String sql = "";
        String nom_produit = this.txt_nom_product.getText();
        String nom_familles_pro = this.CBox_FamillesProduct.getSelectedItem().toString();

        
        seuil_produit = Float.parseFloat(txt_seuil_product.getText());
        
        String seuil = txt_seuil_product.getText();

        if (nom_produit.equals("") || nom_familles_pro.equals("")  || seuil.equals("")) {
            message d = new message(this, true, "Remplissez toutes les cases");
            d.setVisible(true);
        } else {
            sql = "SELECT id_familles AS id FROM familles WHERE nom_familles='" + nom_familles_pro + "'";
            int id_familleProduit = Db.getIdByName(sql);
            sql = "INSERT INTO produits(id_pro,nom_pro,id_familles,seuil_pro) "
                    + "VALUES (null,'" + nom_produit + "'," + id_familleProduit + "," + seuil_produit + ")";

            b = Db.Insert(sql);

            if (b) {
                message f = new message(this, true, "ajout√© avec succ√®s");
                f.setVisible(true);
                this.dispose();
            } else {
                message f = new message(this, true, "pas ajout√©");
                f.setVisible(true);
            }
        }

    }//GEN-LAST:event_btn_add_productActionPerformed

